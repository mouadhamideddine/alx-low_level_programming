#include "3-calc.h"
/**
 * op_add - adds two nums
 * @a : num
 * @b : num
 * Return: Result
 */
int op_add(int a, int b)
{
	return(a + b);
}
/**
 * op_sub - substract
 * @a : num
 * @b : num
 * Return: Result
 */
int op_sub(int a, int b)
{
	return(a - b);
}
/**
 * op_mul - multiply
 * @a : num
 * @b : num
 * Return: result
 */
int op_mul(int a, int b)
{
	return(a * b);
}
/**
 * op_div - divide
 * @a : num
 * @b : num
 * Return: Result
 */
int op_div(int a, int b)
{
	return(a / b);
}
/**
 * op_mod - modulto
 * @a : num
 * @b : num
 * Return: Result
 */
int op_mod(int a, int b)
{
	return(a % b);
}
